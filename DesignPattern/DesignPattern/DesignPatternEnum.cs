namespace DesignPattern
{
    /// <summary>
    /// 设计模式枚举
    /// </summary>
    enum DesignPatternEnum
    {
        /// <summary>
        /// 工厂模式
        /// </summary>
        FactoryPattern,
        /// <summary>
        /// 抽象工厂模式
        /// </summary>
        AbstractFactoryPattern,
        /// <summary>
        /// 单例模式
        /// </summary>
        SingletonPattern,
        /// <summary>
        /// 建造者模式
        /// </summary>
        BuilderPattern,
        /// <summary>
        /// 原型模式
        /// </summary>
        PrototypePattern,
        /// <summary>
        /// 适配器模式
        /// </summary>
        AdapterPattern,
        /// <summary>
        /// 桥接模式
        /// </summary>
        BridgePattern,
        /// <summary>
        /// 过滤器模式（Filter Pattern）或标准模式（Criteria Pattern）
        /// </summary>
        FilterPattern,
    }
}
